import pandas as pd # Import pandas library to work with data tables

# Step 1: Load data and clean missing values
df = pd.read_csv('hourly_weather_10_days.csv')# Read data from the Excel file into a DataFrame

# List all columns that contain numeric weather data
numeric_columns = ['temperature_C', 'humidity_%', 'wind_speed_kmph', 'pressure_hPa', 'visibility_km']

# Fill missing values by carrying the last valid value forward
df[numeric_columns] = df[numeric_columns].ffill()

# Fill any remaining missing values at the start by carrying the next valid value backward
df[numeric_columns] = df[numeric_columns].bfill()

# Step 2: Convert timestamp column to datetime format
df['timestamp'] = pd.to_datetime(df['timestamp'])

# Set the timestamp column as the index for fast date-time lookup
df = df.set_index('timestamp')

# Step 3: Define a function to get weather data for a given date and time
def get_weather(date_time):
    """
    Retrieves and displays weather data for a specific date and time.

    Args:
        date_time (str): Date and time in 'DD/MM/YYYY HH:MM' format.
    """
    # Convert the user input string to a datetime object
    search_time = pd.to_datetime(date_time, format='%d/%m/%Y %H:%M')

    # Check if the requested datetime exists in the dataset index
    if search_time in df.index:
        # Retrieve the row of data for the given timestamp
        record = df.loc[search_time]

        # Print the formatted weather report header
        print(f"\nWeather Report for {search_time.strftime('%d %b %Y, %H:%M')}")
        print("-" * 45) # Print a separator line

        # Print each weather parameter with 2 decimal places
        print(f"Temperature : {record['temperature_C']:.2f} C")
        print(f"Humidity : {record['humidity_%']:.2f} %")
        print(f"Wind Speed : {record['wind_speed_kmph']:.2f} km/h")
        print(f"Pressure : {record['pressure_hPa']:.2f} hPa")
        print(f"Visibility : {record['visibility_km']:.2f} km")
    else:
        # Print an error if the date and time is not found
        print("Error: Data not found for the specified date and time.")

# Step 4: Take input from the user at runtime
user_input = input("Enter date and time (DD/MM/YYYY HH:MM): ") # Ask user for date and time

# Call the function with the user input
get_weather(user_input)