def daily_summary(matrix: np.ndarray):
    summaries = []
    for i in range(matrix.shape[0]):
        day_data = matrix[i, :]
        summaries.append({
            'Day': i + 1,
            'Min_C': float(np.nanmin(day_data).round(2)),
            'Max_C': float(np.nanmax(day_data).round(2)),
            'Mean_C': float(np.nanmean(day_data).round(2))
        })
    return pd.DataFrame(summaries)


